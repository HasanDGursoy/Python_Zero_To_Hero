# Bir dizinin uzunluğunu almak için, standart python fonksiyonu olan len() fonksiyonu kullanılır.

meyve = "portakal"
print(len(meyve)) # Bu şekilde uzunluğunu alabiliyoruz.

print(meyve[-1]) # direk son harfi alabiliriz bu şekilde.

son_index = len(meyve)-1 # bu da son indexe tekabül eder.
print(meyve[son_index])

