# SET

# {} ile set yaratmak

kume = {'at', 'kedi', 'köpek', 'at', 'tavşan', 'kedi'}
print(type(kume))
print(kume)

# set() constuctor ile set yaratmak

k = set({'A', 'B', 'C', 'D', 'E'})
print(k)
print(type(k))

# set() içinde birden fazla eleman yer alamz yazdırsanız yada kullanmaya çalışsanızda set birkere okur yazdırır geçer.

